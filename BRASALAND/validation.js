  function vt(key) {
    const pack = validationText[currentLanguage] || validationText.es;
    return pack[key] || validationText.es[key] || key;
  }

  function isAdult(dateValue) {
    if (!dateValue) return false;
    const birthDate = new Date(dateValue + "T00:00:00");
    if (Number.isNaN(birthDate.getTime())) return false;

    const today = new Date();
    let age = today.getFullYear() - birthDate.getFullYear();
    const monthDiff = today.getMonth() - birthDate.getMonth();

    if (monthDiff < 0 || (monthDiff === 0 && today.getDate() < birthDate.getDate())) {
      age -= 1;
    }

    return age >= 18;
  }

  function setError(fieldKey, message) {
    const input = fields[fieldKey];
    const errorNode = errorNodes[fieldKey];

    if (!input || !errorNode) return;

    errorNode.textContent = message;
    errorNode.classList.remove("hidden");
    input.setAttribute("aria-invalid", "true");
    input.classList.add("border-red-400", "focus:border-red-400", "focus:ring-red-500/40");
  }

  function clearError(fieldKey) {
    const input = fields[fieldKey];
    const errorNode = errorNodes[fieldKey];

    if (!input || !errorNode) return;

    errorNode.textContent = "";
    errorNode.classList.add("hidden");
    input.setAttribute("aria-invalid", "false");
    input.classList.remove("border-red-400", "focus:border-red-400", "focus:ring-red-500/40");
  }

  function validateField(fieldKey) {
    const field = fields[fieldKey];
    const value = field && typeof field.value === "string" ? field.value.trim() : "";

    switch (fieldKey) {
      case "fullName":
        if (!value || !/^\s*\S+\s+\S+.*$/.test(value)) {
          setError(fieldKey, vt("fullName"));
          return false;
        }
        clearError(fieldKey);
        return true;

      case "email":
        if (!value || !/^[^\s@]+@[^\s@]+\.[^\s@]{2,}$/.test(value)) {
          setError(fieldKey, vt("email"));
          return false;
        }
        clearError(fieldKey);
        return true;

      case "phone": {
        if (!value) {
          setError(fieldKey, vt("phone"));
          return false;
        }
        const isValidPhone = /^\+(57|1)\s?\d[\d\s]{7,}$/.test(value);
        if (!isValidPhone) {
          setError(fieldKey, vt("phone"));
          return false;
        }

        if (fields.country.value === "Colombia" && !value.startsWith("+57")) {
          setError(fieldKey, vt("phone"));
          return false;
        }

        if (fields.country.value === "Estados Unidos" && !value.startsWith("+1")) {
          setError(fieldKey, vt("phone"));
          return false;
        }

        clearError(fieldKey);
        return true;
      }

      case "birthDate":
        if (!value || !isAdult(value)) {
          setError(fieldKey, vt("birthDate"));
          return false;
        }
        clearError(fieldKey);
        return true;

      case "country":
        if (!value) {
          setError(fieldKey, vt("country"));
          return false;
        }
        clearError(fieldKey);
        return true;

      case "city":
        if (!value) {
          setError(fieldKey, vt("city"));
          return false;
        }
        clearError(fieldKey);
        return true;

      case "source":
        if (!value) {
          setError(fieldKey, vt("source"));
          return false;
        }
        clearError(fieldKey);
        return true;

      case "terms":
        if (!fields.terms.checked) {
          setError(fieldKey, vt("terms"));
          return false;
        }
        clearError(fieldKey);
        return true;

      default:
        return true;
    }
  }

  function setSelectOptions(select, options, placeholder, shouldDisable) {
    select.innerHTML = "";
    const emptyOption = document.createElement("option");
    emptyOption.value = "";
    emptyOption.textContent = placeholder;
    select.appendChild(emptyOption);

    options.forEach(function (item) {
      const optionNode = document.createElement("option");
      optionNode.value = item.value;
      optionNode.textContent = item[currentLanguage] || item.es;
      select.appendChild(optionNode);
    });

    select.disabled = shouldDisable;
  }

  function updateCityOptions() {
    const selectedCountry = fields.country.value;
    const cities = cityOptions[selectedCountry] || [];
    const cityValue = fields.city.value;

    setSelectOptions(fields.city, cities, vt("cityPlaceholder"), cities.length === 0);
    if (cities.some(function (city) { return city.value === cityValue; })) {
      fields.city.value = cityValue;
    }

    fields.favoriteLocation.value = "";
    setSelectOptions(fields.favoriteLocation, [], vt("locationPlaceholder"), true);
  }

  function updateLocationOptions() {
    const selectedCountry = fields.country.value;
    const selectedCity = fields.city.value;
    const key = selectedCountry + "|" + selectedCity;
    const locations = locationOptions[key] || [];
    const locationValue = fields.favoriteLocation.value;

    setSelectOptions(fields.favoriteLocation, locations, vt("locationPlaceholder"), locations.length === 0);
    if (locations.some(function (location) { return location.value === locationValue; })) {
      fields.favoriteLocation.value = locationValue;
    }
  }

  function refreshDependentSelects() {
    if (!form || !fields || !fields.country || !fields.city || !fields.favoriteLocation) return;
    const countryValue = fields.country.value;
    updateCityOptions();
    fields.country.value = countryValue;
    updateLocationOptions();
  }

